public class NestedLoops {

    public static void main (String[] args ) {

        for(int i = 0; i < 5; i++){
            System.out.println("It is so freaking cold outside!");
        }
        String[] colors = {"Red", "Blue", "Green"};

        for (int i = 0; i<3; i++){
            System.out.println(colors [i]);
        }
    }
}
