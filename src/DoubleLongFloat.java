public class DoubleLongFloat {

    public static void main(String[] args) {

        //int
        int a = 1;
        //long
        long b = 222222222L;
        //float
        float c = 2.3f;
        //double
        double d = 533333333332222245678976545678987654567898.7654;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
    }
}
