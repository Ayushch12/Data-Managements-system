public class Forloops {
    public static void main(String[] args) {


        for (int i = 0; i < 4; i++) {
            System.out.println("I loved dogs!");
        }

        for (int i = 0; i < 10; i++) {
            System.out.println(i);
        }

       for ( int i = 0; i < 20 ; i++){
           System.out.println(i);
       }

       int[] grades = {98, 100, 83, 90, 93};

       for (int i = 0; i < grades.length; i++){
           System.out.println(grades[i]);
       }

    }
}